public class Operadores2 {
    public static void main(String[] args){
        // x repetição

        int numero = 5;
        numero++;
        System.out.println(numero ++);
        System.out.println(numero);

        boolean trueOrFalse = true;

        trueOrFalse = !trueOrFalse;

        System.out.print(trueOrFalse);

    }
}
