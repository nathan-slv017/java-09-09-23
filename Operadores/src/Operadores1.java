public class Operadores1 {
    
    public static void main (String[] args) {
        int numero = 5;

        System.out.println (- numero);
        // -5
        System.out.println (numero);
        // 5
        numero = - numero;
        System.out.println(numero);
        //-5
        numero = numero * -1;
        System.out.println(numero);
        //5
    }

}
